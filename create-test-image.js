const fs = require('fs');

// Create a simple PNG file (minimal PNG structure)
// This creates a 2x2 red pixel PNG
const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR chunk length
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x02, // width: 2
  0x00, 0x00, 0x00, 0x02, // height: 2
  0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, compression, filter, interlace
  0x90, 0x5D, 0x68, 0x82, // CRC
  0x00, 0x00, 0x00, 0x0C, // IDAT chunk length
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x08, 0x99, 0x01, 0x01, 0x00, 0x0C, 0x00, 0xF3, 0xFF, 0xFF, 0x00, 0x00, // compressed data
  0x02, 0x00, 0x01, 0x00, // CRC
  0x00, 0x00, 0x00, 0x00, // IEND chunk length
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC
]);

fs.writeFileSync('/Users/a./Documents/transcendence/test-avatar.png', pngData);
console.log('Test PNG avatar created: test-avatar.png');